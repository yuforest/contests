unordered_map<ll, ll> MEMO;
ll recursive(ll N) {
  if(MEMO.count(N)) {
    debug("MEMO was used");
    debug(N);
    return MEMO[N];
  }
  if(N<= 4) return N;
  ll X1 = N / 2;
  ll X2 = (N + 1) / 2;
  auto res = recursive(X1) * recursive(X2) % mod;
  MEMO[N] = res;
  return res;

  return (res = recursive(X1) * recursive(X2) % mod);
}

